// TASK 1: DECLARING VARIABLES
let studentName = "keegan";      // string
let studentAge = 29;             // number
let isEnrolled = true;           // boolean

console.log("Student Name:", studentName);
console.log("Student Age:", studentAge);
console.log("Is Enrolled:", isEnrolled);

// TASK 2: DATA TYPES
// String
let courseName = "JavaScript Basics";
console.log("String Example:", courseName);

// Number
let score = 95.5;
console.log("Number Example:", score);

// Boolean
let passed = true;
console.log("Boolean Example:", passed);

// Array
let grades = [90, 85, 98, 92];
console.log("Array Example:", grades);

// Object
let student = {
  name: "keegan",
  age: studentAge,
  course: "CPAN 113"
};
console.log("Object Example:", student);

// TASK 3: ARITHMETIC OPERATORS
let a = 10;
let b = 5;

console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);

// TASK 4: COMPARISON OPERATORS
console.log("a > b:", a > b);
console.log("a < b:", a < b);
console.log("a === b:", a === b);
console.log("a !== b:", a !== b);

// TASK 5: LOGICAL OPERATORS
let x = true;
let y = false;

console.log("AND (&&):", x && y);
console.log("OR (||):", x || y);
console.log("NOT (!):", !x);


